# min() max()
y = [10, 100, 24, 11, 0, 98]
# generate a list of random integars
print(min(y))
print(max(y))

# abs()
x = -10
print(abs(x)) # 10 it returns positive values :)
# x^y
print(pow(2, 3)) # 8

import math
# sqrt()
print(int(math.sqrt(16))) # 4.0

# ceil() and floor()
print(math.ceil(2.9)) # 3
print(math.floor(2.9)) # 2
print(math.pi) # 3.141592653589793
print(math.e) # 2.718281828459045